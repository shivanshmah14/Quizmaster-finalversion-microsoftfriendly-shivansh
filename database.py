"""
Database module for QuizMaster - Phase 2
Handles SQLite database operations, user authentication, and CRUD for questions/scores
"""

import sqlite3
from pathlib import Path
import hashlib
import json
import os

DATABASE_PATH = Path(__file__).parent / "data" / "quizmaster.db"
QUESTIONS_JSON_PATH = Path(__file__).parent / "data" / "questions.json"
DATABASE_PATH.parent.mkdir(parents=True, exist_ok=True)
DATABASE_URL = os.getenv("DATABASE_URL", "").strip()
IS_POSTGRES = DATABASE_URL.startswith("postgres://") or DATABASE_URL.startswith("postgresql://")

if IS_POSTGRES:
    try:
        import psycopg2
        from psycopg2.extras import RealDictCursor
    except Exception as e:
        raise RuntimeError(
            "DATABASE_URL is set but psycopg2 is not installed. "
            "Add psycopg2-binary to requirements.txt."
        ) from e


def get_connection():
    if IS_POSTGRES:
        conn = psycopg2.connect(
            DATABASE_URL,
            cursor_factory=RealDictCursor,
            sslmode=os.getenv("DB_SSLMODE", "require"),
        )
    else:
        conn = sqlite3.connect(DATABASE_PATH)
        conn.row_factory = sqlite3.Row
    return conn


def _sql(query):
    if IS_POSTGRES:
        return query.replace("?", "%s")
    return query


def _export_questions_to_json():
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute(
        """
        SELECT id, category, question, options, correct_index, difficulty, points
        FROM questions
        ORDER BY category, id
    """
    )
    rows = cursor.fetchall()
    conn.close()

    categories = {}
    per_category_index = {}
    for row in rows:
        category = row["category"]
        per_category_index[category] = per_category_index.get(category, 0) + 1
        categories.setdefault(category, []).append(
            {
                "id": per_category_index[category],
                "question": row["question"],
                "options": json.loads(row["options"]),
                "correct": row["correct_index"],
                "difficulty": row["difficulty"] or "medium",
                "points": row["points"] if row["points"] is not None else 10,
            }
        )

    payload = {"categories": categories}
    QUESTIONS_JSON_PATH.parent.mkdir(parents=True, exist_ok=True)
    with open(QUESTIONS_JSON_PATH, "w", encoding="utf-8") as f:
        json.dump(payload, f, ensure_ascii=False, indent=2)


def init_database():
    conn = get_connection()
    cursor = conn.cursor()
    if IS_POSTGRES:
        cursor.execute(
            """
            CREATE TABLE IF NOT EXISTS users (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                username TEXT UNIQUE NOT NULL,
                password_hash TEXT NOT NULL,
                is_admin BOOLEAN DEFAULT FALSE,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )
        """
        )
        cursor.execute(
            """
            CREATE TABLE IF NOT EXISTS questions (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                category TEXT NOT NULL,
                question TEXT NOT NULL,
                options TEXT NOT NULL,
                correct_index INTEGER NOT NULL,
                difficulty TEXT DEFAULT 'medium',
                points INTEGER DEFAULT 10,
                created_by BIGINT,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                FOREIGN KEY (created_by) REFERENCES users(id)
            )
        """
        )
        cursor.execute(
            """
            CREATE TABLE IF NOT EXISTS scores (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                user_id BIGINT NOT NULL,
                category TEXT NOT NULL,
                score INTEGER NOT NULL,
                correct_answers INTEGER NOT NULL,
                total_questions INTEGER NOT NULL,
                percentage REAL NOT NULL,
                played_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                FOREIGN KEY (user_id) REFERENCES users(id)
            )
        """
        )
        cursor.execute(
            """
            CREATE TABLE IF NOT EXISTS messages (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                sender_id BIGINT NOT NULL,
                receiver_id BIGINT NOT NULL,
                category TEXT,
                message TEXT NOT NULL,
                sent_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                FOREIGN KEY (sender_id) REFERENCES users(id),
                FOREIGN KEY (receiver_id) REFERENCES users(id)
            )
        """
        )
    else:
        cursor.execute(
            """
            CREATE TABLE IF NOT EXISTS users (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                username TEXT UNIQUE NOT NULL,
                password_hash TEXT NOT NULL,
                is_admin BOOLEAN DEFAULT FALSE,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )
        """
        )
        cursor.execute(
            """
            CREATE TABLE IF NOT EXISTS questions (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                category TEXT NOT NULL,
                question TEXT NOT NULL,
                options TEXT NOT NULL,
                correct_index INTEGER NOT NULL,
                difficulty TEXT DEFAULT 'medium',
                points INTEGER DEFAULT 10,
                created_by INTEGER,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                FOREIGN KEY (created_by) REFERENCES users(id)
            )
        """
        )
        cursor.execute(
            """
            CREATE TABLE IF NOT EXISTS scores (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                user_id INTEGER NOT NULL,
                category TEXT NOT NULL,
                score INTEGER NOT NULL,
                correct_answers INTEGER NOT NULL,
                total_questions INTEGER NOT NULL,
                percentage REAL NOT NULL,
                played_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                FOREIGN KEY (user_id) REFERENCES users(id)
            )
        """
        )
        cursor.execute(
            """
            CREATE TABLE IF NOT EXISTS messages (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                sender_id INTEGER NOT NULL,
                receiver_id INTEGER NOT NULL,
                category TEXT,
                message TEXT NOT NULL,
                sent_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                FOREIGN KEY (sender_id) REFERENCES users(id),
                FOREIGN KEY (receiver_id) REFERENCES users(id)
            )
        """
        )
    conn.commit()
    conn.close()


def hash_password(password):
    return hashlib.sha256(password.encode()).hexdigest()


def create_user(username, password, is_admin=False):
    if not username or not password:
        return False, "Username and password are required"
    if len(password) < 6:
        return False, "Password must be at least 6 characters"
    conn = get_connection()
    cursor = conn.cursor()
    try:
        cursor.execute(
            _sql("INSERT INTO users (username, password_hash, is_admin) VALUES (?, ?, ?)"),
            (username, hash_password(password), is_admin),
        )
        conn.commit()
        return True, "User created successfully"
    except sqlite3.IntegrityError:
        return False, "Username already exists"
    except Exception as e:
        if "unique" in str(e).lower() or "duplicate" in str(e).lower():
            return False, "Username already exists"
        raise
    finally:
        conn.close()


def verify_user(username, password):
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute(
        _sql("SELECT * FROM users WHERE username = ? AND password_hash = ?"),
        (username, hash_password(password)),
    )
    row = cursor.fetchone()
    conn.close()
    if row:
        return {
            "id": row["id"],
            "username": row["username"],
            "is_admin": bool(row["is_admin"]),
            "created_at": row["created_at"],
        }
    return None


def ensure_admin_account(username="admin", password="admin123mas"):
    """
    Ensure there is a default admin account with the expected credentials.
    If the user exists, force admin privileges and update password hash.
    """
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute(_sql("SELECT id FROM users WHERE username = ?"), (username,))
    row = cursor.fetchone()
    password_hash = hash_password(password)
    if row:
        cursor.execute(
            _sql("UPDATE users SET password_hash = ?, is_admin = 1 WHERE username = ?"),
            (password_hash, username),
        )
    else:
        cursor.execute(
            _sql("INSERT INTO users (username, password_hash, is_admin) VALUES (?, ?, 1)"),
            (username, password_hash),
        )
    conn.commit()
    conn.close()


