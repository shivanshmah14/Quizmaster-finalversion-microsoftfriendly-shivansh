    cursor.execute(
        _sql(
            """
        INSERT INTO scores
        (user_id, category, score, correct_answers, total_questions, percentage)
        VALUES (?, ?, ?, ?, ?, ?)
    """
        ),
        (user_id, category, score, correct_answers, total_questions, percentage),
    )
    conn.commit()
    conn.close()


